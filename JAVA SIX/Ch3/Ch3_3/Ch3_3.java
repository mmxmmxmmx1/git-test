//二進位，八進位，16進位
package Ch3.Ch3_3;

public class Ch3_3 {
    public static void main(String[] args) {
        byte bin = 0b1111;
        short oct = 02001;
        int hex1 = 0xff12;
        long hex2 = 0x12345ffeL;

        System.out.println(bin);
        System.out.println(oct);
        System.out.println(hex1);
        System.out.println(hex2);
    }
}
