package Ch3.Ch3_7;

public class Ch3_7 {
    public static void main(String[] args) {
        double num1 = -5.6e64;
        double num2 = -6.32E16;
        float num3 = 2.478f;
        double num4 = 6.3;
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        float n = 5.0f;
        double p = n * n;
        System.out.println(n + "*" + n + "=" + p);

    }

}
